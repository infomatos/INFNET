public class Calculadora {
    public float dividir (int num1, int num2) {
        return num1/num2;
    }
    public float somar (int num1, int num2) {
        return num1+num2;
    }
    public float subtrair (int num1, int num2) {
        return num1-num2;
    }
    public float multiplicar (int num1, int num2) {
        return num1*num2;
    }
}
